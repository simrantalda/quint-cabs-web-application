package com.quint.cabs.beans;

/**
 * @author Simran Talda
 *
 */
public class RideDetails {

	/** cabId */
	private int cabId;

	/** clientId */
	private int clientId;

	/** distance */
	private double distance;

	/** timeTaken */
	private double timeTaken;

	/** carType */
	private String carType;

	/** cost */
	private double cost;
	
	/** toX */
	private double toX;
	
	/** toY */
	private double toY;
	
	/** destinationX */
	private double destinationX;
	
	/** destinationY */
	private double destinationY;
	

	/**
	 * @return the cabId
	 */
	public int getCabId() {
		return cabId;
	}

	/**
	 * @param cabId
	 *            the cabId to set
	 */
	public void setCabId(int cabId) {
		this.cabId = cabId;
	}

	/**
	 * @return the clientId
	 */
	public int getClientId() {
		return clientId;
	}

	/**
	 * @param clientId
	 *            the clientId to set
	 */
	public void setClientId(int clientId) {
		this.clientId = clientId;
	}

	/**
	 * @return the distance
	 */
	public double getDistance() {
		return distance;
	}

	/**
	 * @param distance
	 *            the distance to set
	 */
	public void setDistance(double distance) {
		this.distance = distance;
	}

	/**
	 * @return the timeTaken
	 */
	public double getTimeTaken() {
		return timeTaken;
	}

	/**
	 * @param timeTaken
	 *            the timeTaken to set
	 */
	public void setTimeTaken(double timeTaken) {
		this.timeTaken = timeTaken;
	}

	/**
	 * @return the carType
	 */
	public String getCarType() {
		return carType;
	}

	/**
	 * @param carType
	 *            the carType to set
	 */
	public void setCarType(String carType) {
		this.carType = carType;
	}

	/**
	 * @return the cost
	 */
	public double getCost() {
		return cost;
	}

	/**
	 * @param cost
	 *            the cost to set
	 */
	public void setCost(double cost) {
		this.cost = cost;
	}

	/**
	 * @return the toX
	 */
	public double getToX() {
		return toX;
	}

	/**
	 * @param toX the toX to set
	 */
	public void setToX(double toX) {
		this.toX = toX;
	}

	/**
	 * @return the toY
	 */
	public double getToY() {
		return toY;
	}

	/**
	 * @param toY the toY to set
	 */
	public void setToY(double toY) {
		this.toY = toY;
	}

	/**
	 * @return the destinationX
	 */
	public double getDestinationX() {
		return destinationX;
	}

	/**
	 * @param destinationX the destinationX to set
	 */
	public void setDestinationX(double destinationX) {
		this.destinationX = destinationX;
	}

	/**
	 * @return the destinationY
	 */
	public double getDestinationY() {
		return destinationY;
	}

	/**
	 * @param destinationY the destinationY to set
	 */
	public void setDestinationY(double destinationY) {
		this.destinationY = destinationY;
	}

	
}
